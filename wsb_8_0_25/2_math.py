import math

liczba = 16

wynik = math.sqrt(liczba)
print(f"Pierwiastek kwadratowy z liczby {liczba} wynosi: {wynik}")

#ĆW - oblicz pole koła
promien = 2

pole_kola = math.pi * math.pow(promien, 2)
print(f"Pole koła o promieniu równym {promien} wynosi: {pole_kola}")
#lub
pole2 = math.pi * (promien ** 2)
print(f"Pole koła o promieniu równym {promien} wynosi: {pole2}")